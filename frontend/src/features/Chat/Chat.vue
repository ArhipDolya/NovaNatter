<template>
  <div class="chat-container">
    <h1>NovaNatter Chat</h1>
    <h2>Your username: <span>{{ username }}</span></h2>
    <form @submit.prevent="sendMessage" class="message-form">
      <input v-model="messageText" type="text" autocomplete="off" class="message-input" />
      <button type="submit" class="send-button">Send</button>
    </form>
    <ul class="message-list">
      <li v-for="(message, index) in messages" :key="index" class="message">
        <div class="user-info">
          <router-link to="/profile">
            <img src="\src\assets\user-avatar.png" alt="User Avatar" class="user-avatar" />
          </router-link>
          <div class="message-content">{{ message.message }}</div>
        </div>
      </li>
    </ul>
  </div>
</template>

<script>

import { API_BASE_URL } from '../config';


export default {
  data() {
    return {
      username: null,
      messageText: '',
      messages: [],
      ws: null,
    };
  },
  created() {
    this.fetchUserInfo();
  },
  methods: {
    async fetchUserInfo() {
      try {
        const response = await fetch(`${API_BASE_URL}/auth/user/me`, {
          method: 'GET',
          credentials: 'include'
        });

        if (!response.ok) {
          // User is not logged in, redirect to login page
          document.location.href = '/login'
          return;
        }

        const userInfo = await response.json();
        this.username = userInfo.username;

        if (this.username) {
          this.initWebSocket();
          this.fetchLastMessages();
        }
      } catch (error) {
        console.error('Error fetching user info:', error);
      }
    },
    initWebSocket() {
      this.ws = new WebSocket(`ws://localhost:8000/chat/ws/${this.username}`);
      this.ws.onmessage = this.handleMessage;
    },
    async fetchLastMessages() {
      const response = await fetch('http://localhost:8000/chat/last_messages', {
        method: 'GET'
      });
      const messages = await response.json();
      this.messages = messages;
    },
    appendMessage(msg) {
      this.messages.push({ content: msg });
    },
    sendMessage() {
      const messageData = {
        username: this.username,
        message: this.messageText,
      };
      this.ws.send(JSON.stringify(messageData));
    
      // Update messages directly instead of reloading the page
      this.appendMessage(this.messageText);
      this.messageText = '';

      // Reload the page after 1000 milliseconds (1 second)
      setTimeout(() => {
        location.reload();
      }, 500);
    },
  }
};
</script>

